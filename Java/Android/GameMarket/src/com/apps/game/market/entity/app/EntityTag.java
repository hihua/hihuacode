package com.apps.game.market.entity.app;

public class EntityTag {
	private long id;
	private String name;
	private long count;

	public long getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public long getCount() {
		return count;
	}

	public void setId(long id) {
		this.id = id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setCount(long count) {
		this.count = count;
	}
}
